            <p>Generator  set (genset)  sangat  berpengaruh terhadap masyarakat karena fungsinya sebagai penghasil daya listrik pada saat listrik padam. Tidak hanya digunakan oleh perorangan tapi genset juga digunakan pada perusahaan baik perusahaan besar maupun kecil, milik swasta ataupun pemerintah dengan ukuran genset sampai dengan kapasitas 250 KVA (Kilo Volt Ampere).  </p>

<p>Seiring dengan kemajuan teknologi termasuk pada bidang industri, genset merupakan alat yang wajib ada dalam lingkup Sentral Telepon Otomat (STO), karena DWDM dan Metro harus hidup 24 jam. Oleh karena itu genset harus memiliki tangki sebagai penampung bahan bakar minyak (BBM) yang digunakan untuk menghidupkan genset. Kondisi pengukuran terhadap bahan bakar di dalam tangki yang belum efektif dan efisien dari masing masing STO membuat para pekerja (teknisi) sulit mengetahui kapan bahan bakar akan diisi kembali. </p>


<p>Dari kendala diatas, Sistem Monitoring Tangki BBM Yang Terintegrasi Dengan Database sangat diperlukan. Sistem ini dapat memonitor BBM genset secara real time pada tangki genset dengan menggunakan sensor ultrasonik yang diletakkan pada tangki BBM, lalu data sensor yang dihasilkan akan tampil pada database pada personal computer (PC). Database berupa kolom kolom data yang menampilkan hari dan tanggal, ketinggian bahan bakar dalam tangki, volume bahan bakar. User dapat melakukan monitoring tangki genset serta dari fitur yang telah dirancang pada aplikasi personal computer. 
